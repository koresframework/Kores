package com.koresframework.kores.serialization

import com.koresframework.kores.data.SerData
import com.koresframework.kores.data.Key
import com.koresframework.kores.data.KoresData
import com.koresframework.kores.data.SerKey
import kotlinx.serialization.KSerializer
import kotlinx.serialization.builtins.MapSerializer
import kotlinx.serialization.builtins.serializer
import kotlinx.serialization.descriptors.SerialDescriptor
import kotlinx.serialization.encoding.Decoder
import kotlinx.serialization.encoding.Encoder

object KoresDataSerializer : KSerializer<KoresData> {
    private val ser = MapSerializer(SerKey.serializer(), SerData.serializer())

    override val descriptor: SerialDescriptor = ser.descriptor

    override fun deserialize(decoder: Decoder): KoresData {
        return KoresData(ser.deserialize(decoder))
    }

    override fun serialize(encoder: Encoder, value: KoresData) {
        ser.serialize(encoder, value.dataMapView)
    }
}